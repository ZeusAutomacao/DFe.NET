        public static string CONTETTYPE
        {
            get { return _contettype; }
        }

        public static string ACCEPT
        {
            get { return _accept; }
        }

        public static string METHOD
        {
            get { return _method; }
        }

        public static string AGENT
        {
            get { return _agent; }
        }
        #endregion

    }
}
